        <b>La Industria Militar</b> <br><br>

        Nueve años más tarde, al evidenciarse el trabajo y la producción que había desempeñado la Industria
        Militar, el Mando emitió el Acuerdo Gubernativo No. 461-92 de fecha 19 de Junio de 1992, por medio del
        cual se conforma la Industria Militar, como una Dependencia Militar Auxiliar del Ministerio de la
        Defensa Nacional, sus funciones incluían, no sólo la fabricación de los artículos de abastecimiento
        militar, sino también su distribución comercial para el personal del Ejército, la ubicación permaneció
        en el local original, pero como una unidad totalmente independiente del Servicio de Intendencia del
        Ejército. <br><br>

        Unos meses más tarde, por medio del Acuerdo Gubernativo No. 247-2012 de fecha 15 de Octubre de 1992, se
        modificó el Acuerdo de creación de la Industria Militar, ampliando sus funciones de comercialización de
        productos hacia otros organismos del estado, entidades descentralizadas y autónomas, unidades ejecutoras
        y empresas públicas, estatales y municipales; con la finalidad de que los ingresos por tales
        operaciones, debían de cubrir los costos de producción y gastos de administración para un desarrollo
        sostenible y autofinanciable. <br><br>

        Tales conceptos permitieron que por medio del Acuerdo Gubernativo No. 266-98 de fecha 12 de mayo de
        1998, se adicionaron a las funciones de la Industria Militar la adquisición de todo tipo de artículos
        para uso militar que incluía equipo individual, insignias de metal, tela, cinchos militares
        condecoraciones, distintivos, escudos, gafetes, galones y monogramas para cubrir las necesidades de
        adquisición del personal militar y nuevamente en el año 2005, por medio del Acuerdo Gubernativo No.
        649-2005 de fecha 7 de Diciembre del 2005, se modificó para autorizar a la Industria Militar la
        adquisición de armas defensivas y armas ofensivas de uso individual, para ser comercializadas por el
        Ejército de Guatemala, los cuerpos de seguridad que pertenezcan o estén bajo el control del Ministerio
        de Gobernación y los Cuerpos de seguridad de los organismos del Estado que sean responsables de la
        seguridad de los funcionarios de gobierno, manteniendo los ingresos para fines de funcionamiento de este
        comando. <br><br>

        A lo largo de años más recientes, la Industria Militar ha sido objeto de diversos honores particulares
        en relación a la alta calidad de su producción que incluye premios a nivel nacional como la Ceiba de Oro
        e internacionales como el Clio, galardones que validan el alto nivel profesional y de producción que se
        han venido desarrollando desde la fundación de esta importante, pero discreta unidad del Ejército de
        Guatemala. <br><br>

        Hacia el año 2008, finalmente la Industria Militar fue objeto de una nueva modernización en sus
        instalaciones, específicamente en la fachada, oficinas administrativas y más importante aún, el área de
        ventas para atención al público, que se ha convertido en la razón de ser y de mejoras constantes en la
        calidad de producción y de la adquisición de productos de alto nivel. <br><br>
