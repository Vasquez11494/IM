        <p style="text-align: justify;">
            El primer tercio del siglo XX presentó directrices y normas a cumplir en la elaboración de la vestimenta
            y equipo utilizado por el Ejército; sin embargo, se evidenció una controversia, ya que los elementos con
            mayores recursos económicos, cubrían sus necesidades por medio de atuendos de mejor calidad, por lo que
            se asignó a la Tesorería Nacional, que cubriera los gastos de elaboración que permitieran mantener la
            uniformidad, aunque sin indicarse quienes eran los asignados para confeccionarlos. <br><br>

            Con el Reglamento de 1933, modificada en 1939, se proporciona no sólo la descripción, diseño y color de
            los uniformes y equipo, sino que establece los materiales a utilizarse, lo que genera un verdadero
            equilibrio en la confección del vestuario y aunque no existe la imposición de los sastres a contratar,
            si se generaliza la preferencia por la Sartorial “Imperio” para los uniformes de los Oficiales;
            adicionalmente y para mantener el control de los diseños y modelos, se había emitido por medio del
            Acuerdo del 3 de Diciembre de 1938 “Las Reformas que se indican en el vestuario del Ejército” en cuyo
            punto 3º. Cita: <br><br>

            3º. El Estado Mayor del Ejército queda encargado de exhibir (ortografía original) los modelos
            respectivos a que se refiere esta disposición transitoria, y a los cuales deben atenerse todos los
            interesados, siendo absolutamente prohibido alterar las formas y dimensiones establecidas por la
            presente, modificarlas o hacer agregados caprichosos en ellas*. <br><br>

            Para 1936 ya se encuentra incluido en la organización del Ejército como un Servicio General, el de
            Intendencia cuya misión es proporcionar y atender los recursos en metálico, alimentos, vestuario, equipo
            y alojamiento; adicionalmente se menciona la adquisición de materia prima para la eficiente
            transformación y final utilización, pero no se especifica el concepto de la elaboración del equipo o
            vestuario, por lo que probablemente designó la confección a sastres particulares, ampliándose en 1941 la
            división de la Intendencia General del Ejército en dos departamentos, el de Intendencia General Militar
            y el Departamento Central de Intendencia, asignándosele al último un doble carácter de industrial y
            almacén, ya que estaba destinado a suministrar subsistencias, equipo, menaje, vestuario y todo lo
            necesario para el consumo y mantenimiento del Ejército, especificándose en el Reglamento Interno del
            Servicio de Intendencia la siguiente misión. <br><br>

            Artículo 4º.- En cuanto a su Departamento Central de Intendencia, a la Intendencia General corresponde:
            a) El establecimiento y mantenimiento de fábricas militares, industrias, factorías y talleres,
            necesarios a la producción de materias para la provisión y dotación del Ejército; servicios de
            transporte, abastecimiento y acarreo, granjas agrícolas; cría caballar y remonta; almacenes de
            suministros, etcétera**. <br><br>

            *Méndez, Rosendo P. (compilador) Recopilación de Leyes de la República de Guatemala. Tipografía Nacional
            de Guatemala, Guatemala, Agosto, 1940. P.p. 628 - 629 <br><br>


            **Reglamento del Servicio de Intendencia del Ejército. Secretaría de Guerra, Estado Mayor del Ejército,
            Tipografía Nacional de Guatemala, 1938. P.p. 9. <br><br>

            Evidenciándose la función de tipo industrial, que debió de haber implicado la actividad de elaboración,
            pero sin detectarse alguna fuente que indique la ubicación física de los talleres.<br><br>

            Para 1938, con la publicación de una actualización del Reglamento de Intendencia del Ejército se
            menciona en el Capítulo VI, que corresponde al Departamento Central de Intendencia, las siguientes
            funciones:<br>
            “…organizar y reglamentar las reparticiones que se tienen previstas, como son: establecimientos
            fabriles; Intendencias departamentales y de puertos; servicios de transportes, abastecimiento y acarreo;
            cría caballar y remonta; almacenes para suministros; depósitos, parques, talleres , etcétera*.”<br><br>

            * Reglamento del Servicio de Intendencia del Ejército. Secretaría de Guerra, Estado Mayor del Ejército,
            Guatemala, 1938. P.p. 24<br><br>

            Lo que evidencia que aunque se mencionan los establecimientos fabriles y talleres, no se ha detectado
            ninguna fuente que mencione la ubicación y forma de trabajo. Luego de la Revolución del 20 de Octubre de
            1944 se estableció una nueva organización para el Ejército de Guatemala, que cobró vigencia el 15 de
            Marzo de 1945, incluyendo el Departamento del Cuartel Maestre y el Departamento de Maestranza en cuyo
            reporte de labores de 1945 cita como parte de sus responsabilidades:<br><br>

            <b>DEPARTAMENTO DEL CUARTEL MAESTRE</b> <br>
            Este Departamento que inició sus labores el 1º. de Agosto, tiene a su cargo la adquisición,
            almacenamiento y distribución de todos los artículos necesarios para la alimentación, equipo y vestuario
            del Ejército <br>
            a) Vestuario: Se han proporcionado uniformes de tropa a todas las unidades militares de la República,
            por intermedio del Departamento de Maestranza, encargado en la actualidad de los almacenes instalados en
            el edificio que ocupó la Guardia de Honor, así como de la sastrería de dicho Cuerpo.
            b) Calzado: se ha suministrado el calzado para la tropa a todas las Zonas Militares que lo han
            solicitado, tratándose en la actualidad de implantar un modelo denominado bota “Comando”.<br><br>

            <b>DEPARTAMENTO DE MAESTRANZA</b>
            A cargo de este Departamento quedaron todos los almacenes y talleres del Ramo de la Defensa Nacional.
            Provisionalmente tiene instaladas sus oficinas en el Cuartel que ocupa el Tercer Regimiento y Cuerpo
            Motomecanizado.<br><br>

            *Memorias de la Secretaría de la Guerra y del Ministerio de la Defensa Nacional. Tipografía Nacional de
            Guatemala, Guatemala, marzo 1945. P.p. 3, 36 y 37<br><br>

            Lo que brinda por primera vez, una ubicación, en específico en las instalaciones del edificio que
            actualmente ocupa la Brigada de Policía Militar Guardia de Honor y posteriormente trasladadas; sin
            embargo, hasta este momento no existe una designación como tal de la Industria Militar para el país, y
            aunque se tiene conocimiento del funcionamiento de las sastrerías, básicamente se ubicaron como puntos
            de almacenaje y distribución del vestuario oficial, sin especificar el área de talleres donde se
            concretaba la confección de los mismos.<br><br>

            De acuerdo a lo que norma la Circular No. 1, emitida por el Estado Mayor del Ejército el 11 de Marzo de
            1947, las especificaciones de funciones en cuanto al abastecimiento y provisión de equipo, vestuario,
            alimentación, construcciones, reparaciones, adquisiciones, etc., correspondía al Departamento del
            Cuartel Maestre y el Departamento de Maestranza estaba a cargo del material de guerra (Armamento,
            municiones, etc.) y transportes, de igual forma, según algunas fuentes ese mismo año, funcionó en la
            17ª. Calle entre 6ª. y 7ª. Avenida de la zona 1, la primera Fábrica de Uniformes con fines de venta,
            pero manteniendo la práctica ya descrita, este local sólo se utilizaba como punto de toma de medidas y
            distribución de los mismos, pues eran confeccionados por sastres que trabajaban en sus talleres y luego
            los trasladaban a ese local para entrega.<br><br>

            En 1949, el Estado Mayor General del Ejército emite la Circular No. 0019 efectiva a partir del
            03MAY1949, norma el costo de las prendas del uniforme, de la siguiente manera:<br><br>

            <b>TABLA DE VALORIZACION VESTUARIO </b><br>
            1. Camisola de algodón color kaky …….. Q. 3.00 <br>
            1. Pantalón de algodón color kaky …….. Q. 4.50 <br>
            1. Birrete de algodón color kaky ………. Q. 0.50 <br>
            1. Camisola de lona color azul …………. Q. 2.50 <br>
            1. Pantalón de lona azul ………………… Q. 3.00<br><br>

            Lo que permitió establecer una estandarización y control sobre los costos de venta para los miembros del
            Ejército, concretándose igualmente una supervisión sobre las cantidades que adquirían en lo individual
            el personal militar. Posteriormente, en 1952 por medio de la Circular No. 0102, el Estado Mayor del
            Ejército, designa al Comisariato Militar* , como la entidad a cargo de verificar la confección y venta
            de los uniformes, ordenándose igualmente que fueran trasladados los talleres de sastrería, hacia un
            costado de aquellas instalaciones y posteriormente la fábrica de calzado, cuya misión era cubrir las
            necesidades y demandas de la Institución; no obstante, con el paso de los años y debido a que algún
            personal militar hace uso de prendas no reglamentadas, el Estado Mayor General del Ejército emitió la
            Circular No. 0208-DAG vigente a partir 28 de Mayo de 1959, por medio de la cual se norma que los
            Segundos Jefes de Cuerpos, Zonas, Bases, Escuelas y demás Dependencias del Ejército, serán los
            encargados de dar riguroso cumplimiento al control del uniforme en su versión autorizada por la
            Superioridad, lo que evidencia una variación en el vestuario que se utilizaba, ya que seguramente se
            adquirían prendas en otros sitios o con otros sastres, lo que venía a variar en la presentación general
            de los modelos normados.<br><br>

            * El Comisariato Militar fue una Dependencia que no se instituyó conforme disposición y basamento legal,
            inicio sus funciones bajo el nombre de “Comisariato del Ejército” el día 1 de Octubre de 1945,
            nombrándose Jefe del mismo al Mayor de Infantería Agustín Moreno Aldana, por medio de la Orden General
            del Ejército No. 38 de fecha 25 de Octubre de 1945.<br><br>
