        La Industria Militar 1983:
        Como se hizo evidente y luego de casi veinte fructíferos años de eficiente trabajo, el Mando visualizó
        la necesidad de incluir en la organización del Ejército la unidad que cubriera en forma específica las
        funciones de confección y fabricación de vestuario y calzado militar, además de la comercialización
        dentro del personal militar, por lo que se emitió el Decreto Ley No. 29-83 de fecha 23 de Mayo de aquel
        año, que se citaba: <br>
        ARTICULO 1º. Crear las Secretarías de la Fuerza Terrestre, de Aeronáutica, de Marina, de Seguridad y de
        Servicios e Industria Militar , quienes dependerán del Jefe del Estado mayor de la Defensa Nacional. <br><br>

        Emitiéndose dos meses más tarde, el Decreto Ley No. 81-83 de fecha 19 de Julio de 1983, en el cual se
        conforma la Industria Militar, como una Dependencia Militar Auxiliar Especial, subordinada directamente
        del Estado Mayor de la Defensa Nacional, cuyo funcionamiento se encontraba bajo las órdenes de un
        Oficial Superior, misma que entró en vigor a partir del 4 de agosto del mismo año <br><br>

        Por medio del mismo Decreto emitido por el Congreso de la República de Guatemala, se conforma la Fábrica
        de Municiones del Ejército, con sede en la Cabecera Departamental de Cobán, Alta Verapaz, que pasó a
        integrar parte de la Industria Militar en el país y directamente subordinada al Ministerio de la Defensa
        Nacional. <br><br>
        <img class="fotos_historia" src="../imagenes/historia Industria/Imagen4.png" alt="">
        <br>
        Durante esta década la Industria Militar incrementó y diversificó el tipo de productos entre los que se
        incluyeron playeras, ropa interior, sábanas, cubrecamas, sobre fundas, pañuelos, trajes deportivos,
        confección de pantalones de lona, comercialización de calzado al mercado civil, lo que implicó la
        necesidad de ampliar la tienda, así como la remodelación y reubicación de las bodegas de materia prima y
        del producto terminado <br>
        Aunque por aspectos de funcionamiento y control, la Industria Militar se consideró una unidad
        independiente, organizacionalmente se le consideró parte del Servicio de Intendencia del Ejército.
