                Posteriormente se hizo evidente la necesidad de contar con instalaciones adecuadas para ubicar los
                talleres para la confección y fabricación de uniformes en el Ejército de Guatemala, lo que llevó a que
                en 1963, el entonces Jefe de Gobierno y Ministro de la Defensa Nacional, Coronel de Infantería Enrique
                Peralta Azurdia, ordenara la construcción de las edificaciones en las cuales se unificarían las Fábricas
