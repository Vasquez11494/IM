                administrativas de la Industria Militar, áreas de bodega y vestidores de señoras, así como pavimentación
                de calles y patios interiores; tales trabajos fueron supervisados por el entonces Servicio de Ingenieros
                del Ejército, bajo la asesoría de Ingeniería del Ministerio de la Defensa Nacional, a cargo del Coronel
